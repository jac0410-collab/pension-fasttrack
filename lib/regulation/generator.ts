import type { CaseFormData } from '@/types';

export function generateRegulation(data: CaseFormData): string {
  return data.pension_type === 'DB'
    ? generateDB(data)
    : generateDC(data);
}

// ──────────────────────────────────────────
// 공통 헬퍼
// ──────────────────────────────────────────
function repText(t: string) {
  return t === 'union'
    ? '이 제도 가입대상 근로자의 과반수가 가입한 노동조합'
    : '근로자의 과반수';
}

function enrollText(t: string) {
  return t === 'immediate'
    ? '이 제도 설정 이후 가입대상이 되는 날을 가입자 자격 취득일로 본다.'
    : '이 제도 설정 이후 가입대상이 되는 날(계속근로기간이 1년이 되는 날)을 가입자 자격 취득일로 본다.';
}

function coverageText(period: string, date?: string) {
  if (period === 'afterSetup') {
    return `이 제도의 설정일 이후 가입자가 이 사업에서 근로를 제공한 날부터 제10조제2항 각 호별 자격 상실의 사유가 발생한 날까지를 가입기간으로 한다.`;
  }
  if (period === 'includesPast') {
    return `① 이 제도의 설정일 이후 가입자가 이 사업에서 근로를 제공한 날부터 제10조제2항 각 호별 자격 상실의 사유가 발생한 날까지를 가입기간으로 한다.
② 제1항에도 불구하고 이 제도의 설정 이전에 이 사업에서 근로를 제공한 기간(이하 '과거근로기간'이라 한다)이 있는 가입자에 대하여는 과거근로기간을 가입기간에 포함한다.`;
  }
  return `① 이 제도의 설정일 이후 가입자가 이 사업에서 근로를 제공한 날부터 제10조제2항 각 호별 자격 상실의 사유가 발생한 날까지를 가입기간으로 한다.
② 제1항에도 불구하고 이 제도의 설정 이전에 이 사업에서 근로를 제공한 기간(이하 '과거근로기간'이라 한다)이 있는 가입자에 대하여는 ${date ?? '특정일'} 이후 과거근로기간을 가입기간에 포함한다.`;
}

function paymentSuffix(cycle?: string) {
  const m: Record<string, string> = {
    annual: '매년 말일',
    semiannual: '매반기 말일',
    quarterly: '매분기 말일',
    monthly: '매월 말일',
  };
  return m[cycle ?? 'annual'] ?? '매년 말일';
}

function providerLine(providers: string[]) {
  return providers
    .map((p) => `    명칭 : ${p},    주소 : 서울시 중구 을지로 35`)
    .join('\n');
}

